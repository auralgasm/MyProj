    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        try {
            FileWriter order = new FileWriter("order.txt");
            BufferedWriter order1 = new BufferedWriter(order);
            
            for(int i = 0; i < jTable1.getRowCount(); i++) {
                for(int j = 0; j < jTable1.getColumnCount(); j++){
                    order1.write((String)jTable1.getModel().getValueAt(i, j)+ " ");
                }
                order1.write("\n____________________\n");
            }
            
            order1.close();
        }
        catch(IOException i) {
            i.printStackTrace();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if(jTextPane1.getText().equals("")||jTextPane2.getText().equals("")||jTextPane3.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Please enter correct data! ");
        }
        else {
            String data[] = {jTextPane1.getText(),jTextPane2.getText(),jTextPane3.getText()};
            
            DefaultTableModel tblModel = (DefaultTableModel)jTable1.getModel();
            tblModel.addRow(data);
            
            JOptionPane.showMessageDialog(this, "Adding Order to tabel Successful!");
            
            jTextPane1.setText("");
            jTextPane2.setText("");
            jTextPane3.setText("");
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tblModel = (DefaultTableModel)jTable1.getModel();
        if(jTable1.getSelectedRowCount() == 1){
            String subtotal = jTextPane1.getText();
            String tax = jTextPane2.getText();
            String total = jTextPane3.getText();
            
            tblModel.setValueAt(subtotal, jTable1.getSelectedRow(), 0);
            tblModel.setValueAt(tax, jTable1.getSelectedRow(), 1);
            tblModel.setValueAt(total, jTable1.getSelectedRow(), 2);
            
            JOptionPane.showMessageDialog(this, "Update Successfully!");
        }
        else {
            if(jTable1.getRowCount()==0) {
                JOptionPane.showMessageDialog(this, "Table is Empty");
            }
            else {
                JOptionPane.showMessageDialog(this, "Please select single row to update.");
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

