    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        double total;
        double subtotal = 0;
        final double TAXRATE = .04;
        double tax;
        
        //Chicken Meals
        if (RF1.isSelected()){
            subtotal = subtotal + 113;
        }
        if (SRF1.isSelected()){
            subtotal = subtotal + 113;
        }
        if (RCF1.isSelected()){
            subtotal = subtotal + 68;
        }
        if (RCF2.isSelected()){
            subtotal = subtotal + 73;
        }
        if (SRF1.isSelected()){
            subtotal = subtotal + 113;
        }
        
        //Burger Meals
        if (HAM1.isSelected()){
            subtotal = subtotal + 28;
        }
        if (HAMF.isSelected()){
            subtotal = subtotal + 43;
        }
        if (CHE1.isSelected()){
            subtotal = subtotal + 38;
        }
        if (CHE2.isSelected()){
            subtotal = subtotal + 63;
        }
        
        //Drinks & Desserts
        if (COLA.isSelected()){
            subtotal = subtotal + 18;
        }
        if (SPRITE.isSelected()){
            subtotal = subtotal + 18;
        }
        if (CHOCO.isSelected()){
            subtotal = subtotal + 33;
        }
        if (PIE.isSelected()){
            subtotal = subtotal + 28;
        }
        
        jTextPane1.setText(Double.toString(subtotal));
        
        subtotal = Double.parseDouble(jTextPane1.getText());
        tax = subtotal * TAXRATE;
        total = tax + subtotal;
        
        jTextPane2.setText(Double.toString(tax));
        jTextPane3.setText(Double.toString(total));
        
    }//GEN-LAST:event_jButton1ActionPerformed

